/**
 * Regenerates src/data/waters/brandenburgWaters.ts from the official LAVB
 * Gewässerkarte API (the data behind https://www.lavb.de/gwsmaps).
 * Run: node scripts/fetch-brandenburg.mjs
 *
 * Data source & © Landesanglerverband Brandenburg e.V. (lavb.de) — used as
 * a personal reference; the official map and Gewässerverzeichnis prevail.
 */
import { writeFileSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const API = 'https://gws.lavb.de/api/gm_all'
const OUT_FILE = join(
  dirname(fileURLToPath(import.meta.url)),
  '../src/data/waters/brandenburgWaters.ts'
)

const res = await fetch(API, {
  headers: { 'User-Agent': 'fishpedia-water-fetch/1.0 (personal permit app)' },
})
if (!res.ok) throw new Error(`LAVB API: HTTP ${res.status}`)
const raw = await res.json()

const clean = (s) => {
  const t = (s ?? '').replace(/\s+/g, ' ').trim()
  return t && t !== 'NULL' ? t : undefined
}

// LAVB spells clubs inconsistently ("KAV Templin" / "KAV Templin e.V." /
// "Kreisanglerverband Templin e. V."); normalize so groups merge.
const cleanClub = (s) => {
  const t = clean(s)
  return (
    t &&
    t
      .replace(/\bKreisanglerverband\b/gi, 'KAV')
      .replace(/\be\.?\s?V\.?\s*$/g, '')
      .replace(/\s+/g, ' ')
      .trim()
  )
}

const waters = raw
  .map((r) => {
    const lat = +parseFloat(r.lat).toFixed(5)
    const lng = +parseFloat(r.lng).toFixed(5)
    const ha = parseFloat(r.groesse)
    return {
      id: clean(r.id),
      name: clean(r.bezeichnung) ?? clean(r.bezeichnung_k),
      lat,
      lng,
      ...(ha > 0 && { ha }),
      ...(cleanClub(r.verein) && { club: cleanClub(r.verein) }),
      ...(clean(r.bemerkung) && { note: clean(r.bemerkung) }),
    }
  })
  .filter((w) => w.id && w.name)
  .sort((a, b) => a.id.localeCompare(b.id, 'de'))

for (const w of waters) {
  if (w.lat < 51.3 || w.lat > 53.6 || w.lng < 11.2 || w.lng > 14.8)
    throw new Error(`${w.id} outside Brandenburg: ${w.lat},${w.lng}`)
}
if (waters.length < 800)
  throw new Error(`suspiciously few waters: ${waters.length}`)
const ids = new Set(waters.map((w) => w.id))
if (ids.size !== waters.length) {
  // a few sections share a water number – disambiguate for React keys
  const seen = new Map()
  for (const w of waters) {
    const n = (seen.get(w.id) ?? 0) + 1
    seen.set(w.id, n)
    if (n > 1) w.id = `${w.id} (${n})`
  }
}

const body = waters.map((w) => `  ${JSON.stringify(w)},`).join('\n')
writeFileSync(
  OUT_FILE,
  `// GENERATED by scripts/fetch-brandenburg.mjs — do not edit by hand.
// Source & © Landesanglerverband Brandenburg e.V. — https://www.lavb.de/gwsmaps
// Fetched: ${new Date().toISOString().slice(0, 10)}

export interface BrandenburgWater {
  /** LAVB water number, e.g. "P 07-02" */
  id: string
  /** name as published, incl. permitted section for rivers/canals */
  name: string
  lat: number
  lng: number
  /** size in hectares */
  ha?: number
  /** managing club */
  club?: string
  /** restrictions as published (German) */
  note?: string
}

export const brandenburgWaters: BrandenburgWater[] = [
${body}
]
`
)
console.log(`wrote ${OUT_FILE} (${waters.length} waters)`)
